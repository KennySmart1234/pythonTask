

subtotal_number = int(input(print("Enter a number subtotal: ")))


gratuity_rate = int(input(print("Enter a number gratuity rate: ")))


subtotal_gratuity_division = gratuity_rate / subtotal_number
gratuity_Result = subtotal_number + subtotal_gratuity_division

print("$", subtotal_gratuity_division, " as gratuity and ", "$", gratuity_Result, " as subtotal", "\n")


    
